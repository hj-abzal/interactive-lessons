// старт: 20 грибов и 10 берёз
// - Меньше грибов (стало 10) => меньше берез (стало 5)
// - Больше грибов (стало 30) => больше берез (стало 15)
// - Меньше березы (стало 5) => меньше грибов (стало 10)
// - Больше березы (стало 15) => больше грибов (стало 30)
// - без изменений -> без изменений
// Положения движка для гриба: 10, 20, 30
// Положения движка для березы: 5, 10, 15
//
// Протокооперация (для океана: актиния и рак отшельник)
// То же самое, что мутуализм. Вместо гриба актиния, вместо березы рак. Старт 20 актиний и 10 раков
//
// для КОНКУРЕНЦИЯ
// старт: 10 волков и 10 лис (или других конкурентов: ели и березы больших)
// - Меньше волков (стало 5) => больше лис (стало 15)
// - Больше волков (стало 15) => меньше лис (стало 5)
// - Меньше лис (стало 5) => больше волков (стало 15)
// - Больше лис (стало 15) => меньше волков (стало 5)
// Положения движка для обоих: 5, 10, 15
//
// для КОММЕНСАЛИЗМ (в лесу)
// старт: 10 маленьких елей и 10 больших берёз
// - Меньше мал елей (стало 5) => без изменений бол берез (10)
// - Больше мал елей (стало 15) => без изменений бол берез (10)
// - Меньше бол. берез (стало 5) => меньше мал. елей (стало 5)
// - Больше бол. берез (стало 15) => больше мал.елей (стало 15)
// Положения движка для обоих: 5, 10, 15
//
// комменсализм в океане:
//     Старт: 10 кучек морских желудей и 2 кита
// - Меньше м.жел (стало 5 кучек) => без изменений бол берез (10)
// - Больше м.жел (стало 15 кучек) => без изменений бол берез (10)
// - Меньше китов (стало 1) => меньше м.жел (стало 5 кучек)
// - Больше китов (стало 3) => больше м.жел (стало 15 кучек)
