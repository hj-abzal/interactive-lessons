// - Меньше мал.берез (стало 5) => без изменений бол. елей (10)
// - Больше мал. берез (стало 15) => без изменений бол. елей (10)
// - Меньше бол. елей (стало 5) => больше мал. берез (стало 15)
// - Больше бол. елей (стало 15) => меньше мал. берез (стало 5)
